'''
2.1[10]: На столе лежат n монеток. Некоторые из них лежат вверх решкой, а некоторые – гербом. 
Определите минимальное число монеток, которые нужно перевернуть, чтобы все монетки были повернуты вверх одной и той же стороной. Выведите минимальное количество монет, которые нужно перевернуть. Количество монет и их положение (0 или 1) пользователь вводит с клавиатуры.
Примеры/Тесты:
Введите кол-во монет>? 5
Положение монеты 0: 0 или 1>? 1...1 0 1 1 0
Кол-во монет, чтобы перевернуть: 2
'''

# Вариант 1:
print("Вариант 1")
# Запрашиваем количество монеток
while True:
    try:
        quant = int(input("\033[32m\033[1mВведите количество монеток: \033[0m"))
        if quant <= 0:
            print("\033[31mОшибка! Количество монеток должно быть больше нуля.\033[0m")
            continue
        break
    except ValueError:
        print("\033[31mОшибка! Введите целое число больше 0.\033[0m")
eagl = tail = 0
for i in range(quant):
    while True:
        try:
            coinSi = int(input(f"\033[34mВведите состояние {i+1}-й монетки (0 - решка, 1 - герб): \033[0m"))
            if coinSi != 0 and coinSi != 1:
                print("\033[33mОшибка! Состояние монетки может быть только 0 или 1.\033[0m")
                continue
            if coinSi == 1:
                eagl += 1
            else:
                tail += 1
            break
        except ValueError:
            print("\033[31mОшибка! Введите число 0 или 1\033[0m")    
if tail > eagl and eagl != 0:
    mass = f"\033[32mПереверните герб, таких монет {eagl}.\033[0m"
elif tail == eagl:
    mass = f"\033[32mПереверните герб или решку, таких монет по {eagl}.\033[0m"
elif tail < eagl and tail != 0:
    mass = f"\033[32mПереверните решку, таких монет {tail}.\033[0m"
elif tail == 0:
    mass = f"\033[32mВсе монетки на стороне герба.\033[0m"   
else:
    mass = f"\033[32mВсе монетки на стороне решки.\033[0m"
print(mass)
    

# Вариант 2:
print("Вариант 2")
while True:
    try:
        coin = int(input("\033[32m\033[1mВведите количество монеток: \033[0m"))
        if coin <= 0:
            print("\033[31mОшибка! Количество монеток должно быть больше нуля.\033[0m")
            continue
        break
    except ValueError:
        print("\033[31mОшибка! Введите целое число больше 0.\033[0m")
# Создаем список монеток и запрашиваем их состояние
lists = []
for i in range(coin):
    while True:
        try:
            state = int(input(f"\033[34mВведите состояние {i+1}-й монетки (0 - решка, 1 - герб): \033[0m"))
            if state != 0 and state != 1:
                print("\033[33mОшибка! Состояние монетки может быть только 0 или 1.\033[0m")
                continue
            lists.append(str(state))
            break
        except ValueError:
            print("\033[31mОшибка! Введите число 0 или 1\033[0m")
# Подсчитываем количество монеток, которые нужно перевернуть
taile = lists.count('0')
penny = coin - taile
count = min(taile, penny)
if taile > penny and penny != 0:
    side = "Переверните герб"
elif taile == penny:
    side = "Переверните герб или решку"
elif taile < penny and taile != 0:
    side = "Переверните решку"
elif taile == 0:
    side = "Все монетки на стороне герба"       
else:
    side = "Все монетки на стороне решки"
print("\033[32mМинимальное количество монеток, которые нужно перевернуть: {}. {}.\033[0m".format(count, side))